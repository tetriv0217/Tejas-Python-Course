name  = "Harry"
#       0  1  2  3  4    positive
#       H  a  r  r  y    string
#      -5 -4 -3 -2 -1    negative
print(name[0:3]) # Har
print(name[-4:-1]) # arr
print(name[1:4]) # arr same as [-4:-1]

